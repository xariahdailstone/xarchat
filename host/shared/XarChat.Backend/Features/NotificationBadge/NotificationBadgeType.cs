namespace XarChat.Backend.Features.NotificationBadge
{
    public record struct NotificationBadgeType(NotificationBadgeTypeKind Kind, int Count)
    {
        public static NotificationBadgeType None { get; } = new NotificationBadgeType(NotificationBadgeTypeKind.None, 0);
        public static NotificationBadgeType Mentions { get; } = new NotificationBadgeType(NotificationBadgeTypeKind.Mentions, 0);
        public static NotificationBadgeType PingsWithCount(int count) => new NotificationBadgeType(NotificationBadgeTypeKind.Pings, count);
    }
}
